public class TextAndInteger {
    private String text;
    private int intNum;

    public TextAndInteger(String text, int intNum) {
        this.text = text;
        this.intNum = intNum;
    }

    public String getText() {
        return text;
    }

    public int getIntNum() {
        return intNum;
    }
}
